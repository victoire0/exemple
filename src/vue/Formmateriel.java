      private void  modifierMateriel() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            int idmateriel = (int) jTable1.getValueAt(selectedRow, 0);
            String sql = "UPDATE Materiel SET nommateriel = ?, marque = ?, categorie = ?, idclient = ? WHERE idmateriel = ?";

            try (Connection conn = this.connect();
                 PreparedStatement pstmt = conn.prepareStatement(sql)) {

                pstmt.setString(1, jTextField1.getText());
                pstmt.setString(2, jTextField2.getText());
                pstmt.setString(3, jComboBox1.getSelectedItem().toString());
                pstmt.setString(4, jComboBox2.getSelectedItem().toString());
                pstmt.setInt(5, idmateriel);
                
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Materiel modifié avec succès");
                visualiserMaterielData();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Erreur lors de la modification des données : " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner un client à modifier");
        }
    }
       private void supprimerMateriel() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String clientId = jTable1.getValueAt(selectedRow, 0).toString();
            String sql = "DELETE FROM Materiel WHERE idmateriel = ?";

            try (Connection conn = this.connect();
                 PreparedStatement pstmt = conn.prepareStatement(sql)) {

                pstmt.setString(1, clientId);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Materiel supprimé avec succès");
                visualiserMaterielData();
            } catch (SQLException e) {
                System.out.println(e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Veuillez sélectionner un Materiel à supprimer");
        }
    }
        private void rechercherMateriel() {
        String searchTerm = jTextField3.getText();
        String sql = "SELECT * FROM Materiel WHERE idmateriel LIKE ? OR nommateriel LIKE ? OR marque LIKE ? OR categorie LIKE ? OR idclient LIKE ?";
        
        try (Connection conn = this.connect();
             PreparedStatement pstmt = conn.prepareStatement(sql)) {
            
            String searchPattern = "%" + searchTerm + "%";
            pstmt.setString(1, searchPattern);
            pstmt.setString(2, searchPattern);
            pstmt.setString(3, searchPattern);
            pstmt.setString(4, searchPattern);
            pstmt.setString(5, searchPattern);

            ResultSet rs = pstmt.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new Object[]{
                        rs.getInt("idmateriel"),
                        rs.getString("nommateriel"),
                        rs.getString("marque"),
                        rs.getString("categorie"),
                        rs.getString("idclient"),
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
