     private Connection connect() {
        String url = "jdbc:mysql://localhost:3306/bdamini";
        String user = "root";
        String password = "";
        Connection conn = null;
        try {
            conn = DriverManager.getConnection(url, user, password);
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        return conn;
    }

    private void infoClients() {
        String sql = "SELECT idclient, nom, prenom FROM Client";
        try (Connection conn = this.connect();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(sql)) {

            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
            while (rs.next()) {
                String idNomClient = rs.getInt("idclient") + " " + rs.getString("nom") + " " + rs.getString("prenom");
                model.addElement(idNomClient);
            }
            jComboBox2.setModel(model);

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
   private void enregistrerMateriel() {
    String sql = "INSERT INTO Materiel(idmateriel, nommateriel, marque, categorie, idclient) VALUES(?, ?, ?, ?, ?)";

    try (Connection conn = this.connect();
         PreparedStatement pstmt = conn.prepareStatement(sql)) {

//        pstmt.setString(1, null);
        pstmt.setString(2, jTextField1.getText());
        pstmt.setString(3, jTextField2.getText());
        pstmt.setString(4, jComboBox1.getSelectedItem().toString());

        String selectedClient = jComboBox2.getSelectedItem().toString();
        int idclient = Integer.parseInt(selectedClient.split(" ")[0]);

        pstmt.setInt(5, idclient);
       
        pstmt.executeUpdate();
        JOptionPane.showMessageDialog(null, "Materiel enregistré avec succès");

    } catch (SQLException e) {
        System.out.println(e.getMessage());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Format ID Client incorrect");
    }
}
   
    private void visualiserMaterielData() {
        String sql = "SELECT idmateriel, nommateriel, marque, categorie, idclient from materiel";
        
        try (Connection conn = this.connect();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(sql)) {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Object[] row = {
                    rs.getInt("idmateriel"),
                    rs.getString("nommateriel"),
                    rs.getString("marque"),
                    rs.getString("categorie"),
                    rs.getString("idclient"),
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erreur lors du chargement des données : " + e.getMessage());
        }
    }
