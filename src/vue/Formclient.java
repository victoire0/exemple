private void enregistrerClient() {
    String sql = "INSERT INTO Client(idclient, nom, prenom, adresse, nationalite, tel) VALUES(?, ?, ?, ?, ?, ?)";

    try (Connection conn = this.connect();
         PreparedStatement pstmt = conn.prepareStatement(sql)) {

        pstmt.setString(1, null);
        pstmt.setString(2, jTextField1.getText());
        pstmt.setString(3, jTextField2.getText());
        pstmt.setString(4, jTextField3.getText());
        pstmt.setString(5, jComboBox1.getSelectedItem().toString());
        pstmt.setString(6, jTextField4.getText());

        pstmt.executeUpdate();
        JOptionPane.showMessageDialog(null, "Client enregistré avec succès");

    } catch (SQLException e) {
        System.out.println(e.getMessage());
    }
}

