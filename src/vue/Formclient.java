    private void visualiserClientData() {
        String sql = "SELECT idclient, nom, prenom, adresse, tel, nationalite FROM Client";
        
        try (Connection conn = this.connect();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(sql)) {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Object[] row = {
                    rs.getInt("idclient"),
                    rs.getString("nom"),
                    rs.getString("prenom"),
                    rs.getString("adresse"),
                    rs.getString("tel"),
                    rs.getString("nationalite")
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erreur lors du chargement des données : " + e.getMessage());
        }
    }
     private void modifierClient() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            int idClient = (int) jTable1.getValueAt(selectedRow, 0);
            String sql = "UPDATE Client SET nom = ?, prenom = ?, adresse = ?, tel = ?, nationalite = ? WHERE idclient = ?";

            try (Connection conn = this.connect();
                 PreparedStatement pstmt = conn.prepareStatement(sql)) {

                pstmt.setString(1, jTextField1.getText());
                pstmt.setString(2, jTextField2.getText());
                pstmt.setString(3, jTextField3.getText());
                pstmt.setString(4, jTextField4.getText());
                pstmt.setString(5, jComboBox1.getSelectedItem().toString());
                pstmt.setInt(6, idClient);

                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Client modifié avec succès");
                visualiserClientData();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Erreur lors de la modification des données : " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner un client à modifier");
        }
    }
     
      private void supprimerClient() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String clientId = jTable1.getValueAt(selectedRow, 0).toString();
            String sql = "DELETE FROM Client WHERE idclient = ?";

            try (Connection conn = this.connect();
                 PreparedStatement pstmt = conn.prepareStatement(sql)) {

                pstmt.setString(1, clientId);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Client supprimé avec succès");
                visualiserClientData();
            } catch (SQLException e) {
                System.out.println(e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Veuillez sélectionner un client à supprimer");
        }
    }
      
      private void rechercherClient() {
        String searchTerm = jTextField5.getText();
        String sql = "SELECT * FROM Client WHERE nom LIKE ? OR prenom LIKE ? OR adresse LIKE ? OR nationalite LIKE ? OR tel LIKE ?";
        
        try (Connection conn = this.connect();
             PreparedStatement pstmt = conn.prepareStatement(sql)) {
            
            String searchPattern = "%" + searchTerm + "%";
            pstmt.setString(1, searchPattern);
            pstmt.setString(2, searchPattern);
            pstmt.setString(3, searchPattern);
            pstmt.setString(4, searchPattern);
            pstmt.setString(5, searchPattern);

            ResultSet rs = pstmt.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new Object[]{
                        rs.getInt("idclient"),
                        rs.getString("nom"),
                        rs.getString("prenom"),
                        rs.getString("adresse"),
                        rs.getString("nationalite"),
                        rs.getString("tel")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

