private Connection connect() {
        String url = "jdbc:mysql://localhost:3306/bdamini";
        String user = "root";
        String password = "";
        Connection conn = null;
        try {
            conn = DriverManager.getConnection(url, user, password);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erreur de connexion : " + e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
        }
        return conn;
    }

 private void enregistrertechnicien() {
    String nom = jTextField1.getText();
    String prenom = jTextField2.getText();
    String genre = jRadioButton1.isSelected() ? "M" : "F";
    String nationalite = (String) jComboBox1.getSelectedItem();
    String dateembauche = jFormattedTextField1.getText();
    String salaire = jTextField3.getText();

    String sql = "INSERT INTO technicien(nom, prenom, genre, nationalite, dateembauche, salaire) VALUES(?, ?, ?, ?, ?, ?)";

    try (Connection conn = connect();
         PreparedStatement pstmt = conn.prepareStatement(sql)) {

        pstmt.setString(1, nom);
        pstmt.setString(2, prenom);
        pstmt.setString(3, genre);
        pstmt.setString(4, nationalite);
        pstmt.setString(5, dateembauche);
        pstmt.setString(6, salaire);
        pstmt.executeUpdate();
    } catch (SQLException e) {
        System.out.println("SQL error: " + e.getMessage());
    }
}

 private void visualiserTechniciensData() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);

    try (Connection conn = connect();
         Statement stmt = conn.createStatement();
         ResultSet rs = stmt.executeQuery("SELECT * FROM technicien")) {

        while (rs.next()) {
            Object[] row = {
                rs.getString("nom"),
                rs.getString("prenom"),
                rs.getString("genre"),
                rs.getString("nationalite"),
                rs.getString("dateembauche"),
                rs.getString("salaire"),
            };
            model.addRow(row);
        }
    } catch (SQLException e) {
        System.out.println("SQL error: " + e.getMessage());
    }
}



