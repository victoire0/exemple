private void jjTable1MouseClicked() {                                         
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow != -1) { 
        jTextField1.setText(jTable1.getValueAt(selectedRow, 0).toString()); 
        jTextField2.setText(jTable1.getValueAt(selectedRow, 1).toString());
        String genre = jTable1.getValueAt(selectedRow, 2).toString();
        if ("M".equals(genre)) {
            jRadioButton1.setSelected(true);
        } else {
            jRadioButton2.setSelected(true);
        }
        jComboBox1.setSelectedItem(jTable1.getValueAt(selectedRow, 3).toString());
        jFormattedTextField1.setText(jTable1.getValueAt(selectedRow, 4).toString());
        jTextField3.setText(jTable1.getValueAt(selectedRow, 5).toString());
    }
}

