 private void modifierTechnicien() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String matricule = jTable1.getValueAt(selectedRow, 0).toString();
            String sql = "UPDATE technicien SET nom = ?, prenom = ?, genre = ?, nationalite = ?, dateembauche = ?, salaire = ? WHERE matricule = ?";

            try (Connection conn = connect(); PreparedStatement pstmt = conn.prepareStatement(sql)) {
                pstmt.setString(1, jTextField1.getText());
                pstmt.setString(2, jTextField2.getText());
                pstmt.setString(3, jRadioButton1.isSelected() ? "M" : "F");
                pstmt.setString(4, jComboBox1.getSelectedItem().toString());
                pstmt.setString(5, jFormattedTextField1.getText());
                pstmt.setString(6, jTextField3.getText());
                pstmt.setString(7, matricule);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Technicien modifié avec succès");
                visualiserTechniciensData();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Erreur lors de la modification des données : " + e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner un technicien à modifier", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }
private void supprimerTechnicien() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String matricule = jTable1.getValueAt(selectedRow, 0).toString();
            String sql = "DELETE FROM technicien WHERE matricule = ?";

            try (Connection conn = connect(); PreparedStatement pstmt = conn.prepareStatement(sql)) {
                pstmt.setString(1, matricule);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Technicien supprimé avec succès");
                visualiserTechniciensData();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Erreur lors de la suppression des données : " + e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner un technicien à supprimer", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }
private void rechercherMateriel() {
        String searchTerm = jTextField4.getText();
        String sql = "SELECT * FROM technicien WHERE matricule LIKE ? OR nom LIKE ? OR prenom LIKE ? OR genre LIKE ? OR nationalite LIKE ? OR dateembauche LIKE ? OR salaire LIKE ?";
        
        try (Connection conn = this.connect();
             PreparedStatement pstmt = conn.prepareStatement(sql)) {
            
            String searchPattern = "%" + searchTerm + "%";
            pstmt.setString(1, searchPattern);
            pstmt.setString(2, searchPattern);
            pstmt.setString(3, searchPattern);
            pstmt.setString(4, searchPattern);
            pstmt.setString(5, searchPattern);
            pstmt.setString(6, searchPattern);
            pstmt.setString(7, searchPattern);
            
            ResultSet rs = pstmt.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new Object[]{
                        rs.getInt("matricule"),
                        rs.getString("nom"),
                        rs.getString("prenom"),
                        rs.getString("genre"),
                        rs.getString("nationalite"),
                        rs.getString("dateembauche"),
                        rs.getString("salaire"),
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

